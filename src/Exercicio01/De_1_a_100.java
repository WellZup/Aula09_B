package Exercicio01;

public class De_1_a_100 {
    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++){
            System.out.println("Saída com for: " + i);
        }

        int k = 1;
        while (k <= 100){
            System.out.println("Saída com while: " + k);
            k++;
        }

    }
}
